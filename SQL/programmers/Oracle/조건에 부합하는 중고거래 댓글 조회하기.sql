/*
이 문제에서 내가 몰랐던 것 : 기간을 지정하는 법

BETWEEN, AND는 알지만 나머지를 모름.
그냥 TO_CHAR()로 날짜를 문자열로 바꾸고 그 중 10월인 것을 고르기
*/

-- 코드를 입력하세요
SELECT USED_GOODS_BOARD.TITLE, USED_GOODS_BOARD.BOARD_ID, USED_GOODS_REPLY.REPLY_ID, USED_GOODS_REPLY.WRITER_ID, USED_GOODS_REPLY.CONTENTS, TO_CHAR(USED_GOODS_REPLY.CREATED_DATE, 'YYYY-MM-DD') AS CREATED_DATE
FROM USED_GOODS_BOARD, USED_GOODS_REPLY
WHERE USED_GOODS_BOARD.BOARD_ID = USED_GOODS_REPLY.BOARD_ID AND TO_CHAR(USED_GOODS_BOARD.CREATED_DATE, 'MM') = 10
ORDER BY USED_GOODS_REPLY.CREATED_DATE ASC, USED_GOODS_BOARD.TITLE ASC;